            ep = prompt_data['episode']
            scene = prompt_data['scene']
            title = prompt_data['title']
            prompt = prompt_data['prompt']

            print("="*80)
            print(f"[{i}/{len(PROMPTS)}] EP{ep} - 씬 {scene}: {title}")
            print("="*80)
            print(f"\n📝 프롬프트 (길이: {len(prompt)}자):")
            print(f"\n{prompt[:150]}...\n")

            print(f"👉 위 프롬프트를 Kling.ai에 입력하고 생성을 시작해주세요.")
            print(f"   (텍스트 전체를 복사하려면 아래 명령을 참고하세요)\n")
            print(f"📋 전체 프롬프트 (복사용):")
            print(f"{prompt}\n")

