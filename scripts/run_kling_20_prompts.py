        print("\n브라우저에서 Kling.ai에 로그인을 완료하세요.")
        print("로그인 후 터미널로 돌아와 아래 지시를 따르세요.\n")

        input("⏳ 로그인을 완료했으면 엔터를 누르세요: ")

        print("\n✅ 프롬프트 입력을 시작합니다!\n")
        time.sleep(2)

        # 프롬프트 처리
